corSite = 'Azul'
corLetras = 'Preto'
corTonalidade = 'azul-claro'
function exibirCor(cor, Letras, tonalidade) {
    corSite = cor
    corLetras = Letras
    corTonalidade = tonalidade
}

console.log(corSite, ',', corLetras, ',',  corTonalidade)
exibirCor('vermelho', 'branco', 'verde-claro')
console.log(corSite, ',', corLetras, ',',  corTonalidade)