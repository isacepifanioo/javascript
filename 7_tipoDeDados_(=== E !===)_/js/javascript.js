let numero = '5'

if (numero === 5) { // comparação === ele vai compara se o dipo de dados e o msm 
    console.log('Esse numero e o 5')
};

// "5" => string
// 5 => number
// se '5' === 5 => false 
// se '5' == 5 => true

let numero2 = 10

if (numero2 !== '10') { // comparação !== ele vai compara se o dipo de dados e diferente
    console.log('Esse numero eo 10')
}

// se '5' !== 5 => true
// se 5 != 5 => false